import { readdir, readFile, rm, mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { repoRoot, resolveSourceDirectory, relativePosix } from './lib/paths.mjs';
import {
  descriptionFromMarkdown,
  removeFirstHeading,
  titleFromMarkdown,
  yamlString,
} from './lib/markdown.mjs';

const outputRoot = path.join(repoRoot, 'src/content/docs/project/reference');

async function collectMarkdownFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) files.push(...(await collectMarkdownFiles(fullPath)));
    if (entry.isFile() && entry.name.endsWith('.md')) files.push(fullPath);
  }

  return files;
}

function buildPage({ markdown, title, description, sourceUrl }) {
  return `---\ntitle: ${yamlString(title)}\ndescription: ${yamlString(description)}\n---\n\n<!-- Generated from ${sourceUrl}. Do not edit this file directly. -->\n\n${removeFirstHeading(markdown)}\n`;
}

export async function syncProjectDocs() {
  const sourceRoot = await resolveSourceDirectory({
    envName: 'GROWRIG_SOURCE_DIR',
    siblingPath: '../growrig',
    fallbackPath: 'source/growrig',
    label: 'the growrig project repository',
  });

  await rm(outputRoot, { recursive: true, force: true });
  await mkdir(outputRoot, { recursive: true });

  const docsRoot = path.join(sourceRoot, 'docs');
  const sources = (await collectMarkdownFiles(docsRoot)).map((filePath) => ({
    filePath,
    relativePath: relativePosix(docsRoot, filePath),
  }));

  for (const rootFile of ['ROADMAP.md', 'CONTRIBUTING.md', 'GOVERNANCE.md']) {
    const filePath = path.join(sourceRoot, rootFile);
    try {
      await readFile(filePath, 'utf8');
      sources.push({ filePath, relativePath: rootFile.toLowerCase() });
    } catch {
      // Optional in the bundled MVP snapshot.
    }
  }

  for (const source of sources) {
    const markdown = await readFile(source.filePath, 'utf8');
    const fallbackTitle = path.basename(source.relativePath, '.md').replaceAll('-', ' ');
    const title = titleFromMarkdown(markdown, fallbackTitle);
    const description = descriptionFromMarkdown(
      markdown,
      `${title} from the GrowRig project documentation.`,
    );
    const outputPath = path.join(outputRoot, source.relativePath);
    const sourceUrl = `https://github.com/growrig/growrig/blob/main/${relativePosix(sourceRoot, source.filePath)}`;

    await mkdir(path.dirname(outputPath), { recursive: true });
    await writeFile(outputPath, buildPage({ markdown, title, description, sourceUrl }), 'utf8');
  }

  return { sourceRoot, count: sources.length };
}
